package engine.input;

/**
 * Enum representing all possible input actions in the engine
 * These are abstract actions that can be mapped to specific keys
 */
public enum InputAction {
    MOVE_UP,
    MOVE_DOWN,
    MOVE_LEFT,
    MOVE_RIGHT,
    ACTION_1,
    ACTION_2,
    PAUSE,
    CONFIRM,
    CANCEL
}
